#include "../../minishell.h"

void	ft_pwd(void)
{
	char	cwd[1024];

	if (!getcwd(cwd, sizeof(cwd)))
	{
		perror("pwd");
		exit(1);
	}
	printf("%s\n", cwd);
	exit(0);
}
